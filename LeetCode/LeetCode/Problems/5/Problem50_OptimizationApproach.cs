    public double MyPow(double x, int n)
    {
        if (x == 1)
            return 1;
        if (x == -1)
            return n % 2 == 1 ? -1 : 1;
        if (n == 0)
            return 1;

        bool isNegative = false;
        if (n < 0)
        {
            isNegative = true;
            n = ~n;
            if (n != int.MaxValue)
                n++;
        }

        var res = ReqPow(x, n);

        return isNegative
            ? 1 /
              (n == int.MaxValue
                  ? res * x
                  : res)
            : res;
    }

    private static double ReqPow(double x, int n)
    {
        if (n == 1)
            return x;
        if(PowersOfLowerPowers.TryGetValue(n, out var tempN))
        {
            Dictionary<int, double> calculatedPowersValues = new ();
            var tempRes = ReqPow(x, tempN);
            calculatedPowersValues.Add(tempN, tempRes);

            while (tempN + tempN < n)
            {
                tempN += tempN;
                tempRes *= tempRes;
                calculatedPowersValues.Add(tempN, tempRes);
            }

            var calculatedPowers = calculatedPowersValues.Keys.Select(p => p).ToArray();
            var largestAvailablePowerIndex = calculatedPowers.Length - 1;
            while (tempN != n)
            {
                while (largestAvailablePowerIndex >= 0)
                {
                    if(tempN + calculatedPowers[largestAvailablePowerIndex] <= n)
                        break;
                    largestAvailablePowerIndex--;
                }

                var largestAvailablePower = calculatedPowers[largestAvailablePowerIndex];

                tempN += largestAvailablePower;
                tempRes *= calculatedPowersValues[largestAvailablePower];
            }
            
            return tempRes;
        }
        else 
        {
            var powOfHalfPower = ReqPow(x, n / 2);
            var res = powOfHalfPower * powOfHalfPower;
            if (n % 2 == 1)
                res *= x;
            return res;
        }
    }

